public class string {
    public static void main(String[] args)
    {
        String hannah = "Did Hannah see bees? Hannah did.";
        System.out.println(hannah.length()); // returns 32
        System.out.println(hannah.charAt(11)); // returns 's'
        System.out.println(hannah.charAt(15)); // returns 'b'
    }
}